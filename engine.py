# --- 2. FORMATTING & PDF (Professional Layout) ---
def format_time(seconds):
    return f"{int(seconds // 60):02d}:{int(seconds % 60):02d}"

def clean_text(text):
    safe_text = text.encode('latin-1', 'ignore').decode('latin-1')
    return " ".join(safe_text.split())

def create_pro_report(summary, segments, actions):
    pdf = FPDF()
    pdf.add_page()
    
    # Header Banner
    pdf.set_fill_color(20, 40, 60) 
    pdf.rect(0, 0, 210, 40, 'F')
    pdf.set_text_color(255, 255, 255)
    pdf.set_font("Arial", 'B', 22)
    pdf.cell(190, 20, txt="AI TECH-SCRIBE INTELLIGENCE", ln=True, align='C')
    pdf.set_font("Arial", 'I', 10)
    pdf.cell(190, 5, txt="GENERATED BY GOOGLE GEN-AI (2026 EDITION)", ln=True, align='C')
    pdf.ln(25)

    # Summary Section
    pdf.set_text_color(20, 40, 60)
    pdf.set_font("Arial", 'B', 14)
    pdf.cell(190, 10, txt="EXECUTIVE SUMMARY", ln=True)
    pdf.line(10, pdf.get_y(), 200, pdf.get_y())
    pdf.ln(4)
    pdf.set_text_color(0, 0, 0)
    pdf.set_font("Arial", '', 11)
    pdf.multi_cell(0, 8, txt=summary)
    pdf.ln(8)

    # Action Items Section
    pdf.set_fill_color(240, 245, 250)
    pdf.set_font("Arial", 'B', 12)
    pdf.set_text_color(20, 40, 60)
    pdf.cell(190, 10, txt=">>> CRITICAL ACTION ITEMS", ln=True, fill=True)
    pdf.set_font("Arial", '', 10)
    pdf.set_text_color(0, 0, 0)
    for item in actions:
        if item.strip():
            pdf.multi_cell(0, 8, txt=f"- {item}")
    pdf.ln(8)

    # Transcript Section
    pdf.set_font("Arial", 'B', 14)
    pdf.set_text_color(20, 40, 60)
    pdf.cell(190, 10, txt="FULL TRANSCRIPT", ln=True)
    pdf.line(10, pdf.get_y(), 200, pdf.get_y())
    pdf.ln(4)
    for seg in segments:
        pdf.set_font("Arial", 'B', 9)
        pdf.set_text_color(100, 100, 100)
        pdf.write(8, f"[{format_time(seg.start)}] SPEAKER: ")
        pdf.set_font("Arial", '', 10)
        pdf.set_text_color(0, 0, 0)
        pdf.write(8, f"{clean_text(seg.text)}\n")
        pdf.ln(2)

    pdf.output("Professional_Report.pdf")

